# 给出一个 32 位的有符号整数，你需要将这个整数中每位上的数字进行反转。

# 示例 1:
# 输入: 123
# 输出: 321

#  示例 2:
# 输入: -123
# 输出: -321

# 示例 3:
# 输入: 120
# 输出: 21


def func(x):
    flag = 1
    if x < 0:
        flag = -1
        x = -x
    x = int(str(x)[::-1])    # 注意：题意是如果反转后的数值溢出，则取0，所以要先执行反转
    if x < -2147483648 or x > 2147483648:
        x = 0
    print(flag * x)
func(1534236469)


